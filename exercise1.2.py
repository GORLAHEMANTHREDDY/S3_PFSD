class rectangle():
    l = int(input("Enter length of rectangle: "))
    b = int(input("Enter breadth of rectangle: "))
    area = l*b
    perimeter = 2*(l+b)
    print(area)
    print(perimeter)
class circle():
    r = int(input("enter the radius of circle:"))
    areacircle = 2*3.14*r
    perimetercircle = 3.14*r*r
    print(areacircle)
    print(perimetercircle)
class triangle():
    length = int(input("Enter length of triangle: "))
    breadth = int(input("Enter breadth of triangle: "))
    height = int(input("Enter height of triangle: "))
    areatriangle = 0.5*breadth*height
    perimetertriangle = length+breadth+height
